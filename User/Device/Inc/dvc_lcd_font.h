static inline uint8_t LCD_Font_Get_Row(char Character, uint8_t Row)
{
    if ((Character >= 'a') && (Character <= 'z'))
    {
        Character = static_cast<char>(Character - 'a' + 'A');
    }

    for (uint8_t i = 0; i < (sizeof(LCD_Font_Characters) - 1U); i++)
    {
        if (LCD_Font_Characters[i] == Character)
        {
            return LCD_Font_5x7[i][Row];
        }
    }

    return LCD_Font_5x7[sizeof(LCD_Font_Characters) - 2U][Row];
}

